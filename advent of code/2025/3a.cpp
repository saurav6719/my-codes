int dp[105][15];
int f(string &s, int i, int j){
    int n = s.size();
    if(i == n) return 0;
    if(j == 0) return 0;
    if(dp[i][j] != -1) return dp[i][j];
    int ans = 0;
    // choose
    {
        int remain = j - 1;
        int x = 1;
        while(remain--){
            x = x * 10;
        }
        int val = s[i] - '0';
        ans = max(ans, val * x + f(s, i + 1, j - 1));
    }
    // not choose
    {
        int remaining = j;
        int remainingidx = n - 1 - i;
        if(remainingidx >= remaining){
            ans = max(ans, f(s, i + 1, j));
        }
    }
    return dp[i][j] = ans;
}
void solve(){
    string s;
    int ans = 0;

    while(cin>>s){
        memset(dp, -1, sizeof(dp));
        ans += f(s, 0, 2);
    }

    cout<<ans<<endl;
}
