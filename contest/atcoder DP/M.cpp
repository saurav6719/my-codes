int dp[105][100005];
void solve(){
    int n;
    int k;
    cin>>n>>k;
    vector<int> a(n);
    for(int i = 0; i<n; i++){
        cin>>a[i];
    }
    
    for(int i = 0; i<=a[0]; i++){
        dp[0][i] = 1;
    }

    for(int i = 1; i<n; i++){
        vector<int> prf(k+1);
        prf[0] = dp[i-1][0];
        for(int j = 1; j<=k; j++){
            prf[j] = prf[j-1] + dp[i-1][j];
            prf[j] %= mod;
        }

        for(int j = 0; j<=k; j++){
            if(j>a[i]) dp[i][j] = prf[j] - prf[j-a[i]- 1] + mod;
            else dp[i][j] = prf[j]; 
            dp[i][j] %= mod;
        }
    }

    cout<<dp[n-1][k];



    

    
}
