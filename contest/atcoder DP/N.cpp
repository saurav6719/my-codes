int dp[401][401];
int prf[401];

int f(int i, int j){
    if(i==j) return 0;
    int ans = 1e15;

    if(dp[i][j] != -1) return dp[i][j];

    for(int k = i; k<=j-1; k++){
        ans = min(ans, f(i,k) + f(k+1, j) + prf[j] - (i>0 ? prf[i-1] : 0));
    }

    return dp[i][j] = ans;

}
void solve(){
    int n;
    cin>>n;
    vector<int> input(n);
    for(int i = 0; i<n; i++){
        cin>>input[i];
    }

    memset(dp, -1, sizeof dp);

    prf[0] = input[0];

    for(int i = 1; i<n; i++){
        prf[i] = prf[i-1] + input[i];
    }

    cout<<f(0,n-1);

    
}
