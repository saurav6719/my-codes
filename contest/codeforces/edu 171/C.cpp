void solve(){
    int n;
    cin>>n;
    string str;
    cin>>str;
    str = '#' + str;
    queue<int> onequ;
    int ans = 0;
    for(int i = n; i>=1; i--){
        if(str[i] == '1'){
            onequ.push(i);
        }

        if(str[i] == '0'){
            ans += i;
            if(onequ.size() > 0){
                onequ.pop();
            }
        }
    }

    vector<int>v;

    while(!onequ.empty()){
        v.push_back(onequ.front());
        onequ.pop();
    }

    sort(v.begin(),v.end());

    deque<int> dq;
    for(int i = 0; i<v.size(); i++){
        dq.push_back(v[i]);
    }

    while(!dq.empty()){
        ans += dq.front();
        dq.pop_front();
        if(dq.size() > 0)dq.pop_back();
    }

    cout<<ans<<endl;


}
