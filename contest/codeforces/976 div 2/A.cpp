long long largestPowerLE(int k, int n) {
    if (k == 1) return 1; // If k is 1, the only power is 1

    long long currentPower = 1; // Start with k^0 = 1
    long long largest = 1;      // Initialize largest to k^0
    while (currentPower <= n) {
        largest = currentPower; // Update largest valid power
        if (currentPower > LLONG_MAX / k) break; // Prevent overflow
        currentPower *= k; // Calculate the next power of k
    }

    return largest;
}
