vector<pp> f(string&s) {
    int n = s.size();
    map<int, vector<int> > prf;
    vector<pp> v;

   
    int ps = 0;
    prf[0].push_back(0); 

    
    for (int i = 0; i < n; i++) {
       
        ps += (s[i] == '1') ? 1 : -1;

       
        if (prf.find(ps) != prf.end()) {
            vector<int> &positions = prf[ps];
            for (int ele : positions) {
                v.emplace_back(ele + 1, i + 1); 
            }
        }
        prf[ps].push_back(i + 1);
    }

    return v;
}


void solve(){
    string s;
    cin>>s;
    int n = s.size();

    vector<pp> result = f(s);

    int cnt= 0;

    for(auto ele : result){
        int start = ele.first;
        int end = ele.second;

        int xx = n-end+1;
        int yy = start;
        cnt += xx*yy;
        cnt %= mod;
    }

    cout<<cnt%mod<<endl;
}
