std::vector<int> generateKDistinctNumbers(int k, int n) {
    std::vector<int> result;

    // Generate first k-1 distinct numbers
    for (int i = 1; i < k; ++i) {
        result.push_back(i);
        n -= i;  // Subtract from n to maintain the required sum
    }

    // Add the last number to ensure the sum equals n
    result.push_back(n);

    // If the last number coincides with any of the previous numbers, adjust the sequence
    if (n >= 1 && n < k) {
        result[k-1] += k;  // Adjust to make the last number distinct
        result[0] -= k;    // Adjust the first number to maintain the sum
    }

    return result;
}


