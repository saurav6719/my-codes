int sum(int x){

    if(x <= 0) return 0;
    if(x % 2 == 0){
        return ((x/2) % mod * ((x+1) % mod)) % mod;
    }
    if((x+1) % 2 == 0){
        return (((x+1)/2) % mod * (x % mod)) % mod;
    }
}
