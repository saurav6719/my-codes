void solve(){
    int n;
    cin>>n;
    string str;

    cin>>str;
    str = "#" + str ;
    vector<int> left(n+1, 0);
    vector<int >right(n+1);

    for(int i = 1; i<=n; i++){
        if(str[i] == 'R'){
            left[i] = 0;
            continue;
        }
        else{
            if(str[i-1] == 'R'){
                left[i] = 2 + left[i-2];
            }
            else left[i] = 1;
        }
    }  

    for(int i = n-1; i>=0; i--){
        if(str[i+1] == 'L'){
            right[i] = 0;
            continue;
        }
        else{
            // agla r hai 
            if(i+2 > n) {
                right[i] = 1;
                continue;
            }
            if(str[i+2] == 'L'){
                right[i] = 2 + right[i+2];
                continue;
            }
            else right[i] = 1;
        }
    } 

    print(left);
    print(right);

    for(int i= 0; i<=n; i++){
        cout<<left[i] + right[i] + 1<<" ";
    }
    cout<<endl;


}
