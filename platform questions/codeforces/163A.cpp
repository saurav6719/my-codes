int dp[5005][5005];
int f(string &str1, string &str2, int i, int j){
    if(i>= str1.size() or j>= str2.size()) return 0;
    if(dp[i][j] != -1) return dp[i][j];
    int ans =0;
    ans += f(str1, str2, i, j+1);
    ans %= mod;
    ans %= mod;
    if(str1[i] == str2[j]){
        ans += 1+f(str1, str2, i+1, j+1);
        ans %= mod;
    }
    return dp[i][j] = ans;
}
