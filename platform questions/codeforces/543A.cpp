/* write core logic here */
int dp[502][502];
int previ[502][502];
void solve(){
    int n,m,b;
    int mod;
    cin>>n>>m>>b>>mod;

    vector<int> bugs(n);
    for(int i= 0; i<n; i++){
        cin>>bugs[i];
    }

    memset(dp, 0, sizeof dp );
    memset(previ, 0, sizeof previ);

    previ[0][0] = 1;

    for(int i= 1; i<=n; i++){
        memset(dp, 0, sizeof dp );
        for(int j = 0; j<=m; j++){
            for(int k = 0; k<=b; k++){
                if(j>=1 and k-bugs[i-1] >= 0) dp[j][k] += dp[j-1][k-bugs[i-1]];
                dp[j][k] %= mod;
                dp[j][k] += previ[j][k];
                dp[j][k] %= mod;
            }
        }
        swap(previ, dp);
    }


    int ans = 0;
    for(int i = 0; i<=b; i++){
        ans += previ[m][i];
        ans %= mod;
    }

    cout<<ans<<endl;
}
/* logic ends */

