void solve(){
    int n,k;
    cin>>n>>k;
    vector<int> input(n);
    for(int i = 0; i<n; i++){
        cin>>input[i];
    }

    vector<vector<int> > dp(n+1, vector<int> (k+1, 1e16));
    // memset(dp, INT_MAX, sizeof dp);

    for(int i = 0; i<=k; i++){
        dp[0][i] = 0;
    }


    // dp[i][j] = min sum till starting ke i elements pe j operation kr chuke hai 


    for(int i = 1; i<=n; i++){
        for(int j = 0; j<=k; j++){
            int mini = INT_MAX;
            for(int pos = i; i-pos <= j and pos >=1 ; pos--){
                mini = min(mini , input[pos-1]);

                int xx = mini * (i-pos+1);
                xx += dp[pos-1][j - (i - pos)];

                dp[i][j] = min(dp[i][j] , xx);
            }
        }
    }

    int asn = 1e16;

    for(int i = 0; i<=k; i++){
        asn = min(asn, dp[n][i]);
    }

    cout<<asn<<endl;


}
/* logic ends */

