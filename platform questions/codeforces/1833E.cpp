int cyclecnt;
int n;
bool cycle;
vector<set<int> > graph;

void dfs(int i, vector<int> &visited){
    visited[i] = 1;
    if(graph[i].size() == 1){
        cycle = false;
    }
    for(auto ele :graph[i]){
        if(visited[ele] == -1){
            dfs(ele, visited);
        }
    }
}
void solve(){
    cin>>n;

    graph.clear();
    graph.resize(n+5);
    

    for(int i = 1; i<=n; i++){
        int ele;
        cin>>ele;

        graph[i].insert(ele);
        graph[ele].insert(i);
    } 


    int cnt = 0;
    cyclecnt = 0;
    vector<int> visited(n+5, -1);
    for(int i =1; i<=n; i++){
        if(visited[i] == -1){
            cnt++;
            cycle = true;
            dfs(i,visited);
            if(cycle == true) cyclecnt++;
        }
    }


    int mini = min(1+cyclecnt , cnt);
    int maxi = cnt;
    cout<<mini<<" "<<maxi<<endl;



}
