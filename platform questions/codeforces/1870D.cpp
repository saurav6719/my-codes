void solve(){
    int n;
    cin>>n;
    vector<int> input(n);
    for(int i =0 ; i<n; i++){
        cin>>input[i];
    }
    int k;
    cin>>k;

    for(int i = n-2; i>=0; i--){
        input[i] = min(input[i], input[i+1]);
    }

    vector<int> ans(n, 0);
    ans[0] += (k/input[0]);
    k-= (ans[0]*input[0]);

    for(int i= 1; i<n; i++){
        if(input[i] == input[i-1]){
            ans[i] = ans[i-1];
            continue;
        }
        int diff = input[i] - input[i-1];
        ans[i] =min(ans[i-1] ,k/ diff);
        k -= (ans[i]*diff);
    }
    for(int i = 0; i<n; i++){
        cout<<ans[i]<<" ";
    }
    cout<<endl;
}
