void solve(){
    int n;
    cin>>n;
    vector<int> input(n);
    for(int i = 0; i<n; i++){
        cin>>input[i];
    }

    vector<int> dp;
    dp.push_back(0);
    set<int> st;
    st.insert(0);
    for(int i = 0 ; i<n; i++){
        int sz = dp.size();
        for(int j = 0 ; j<sz; j++){
            int newi = input[i] + dp[j];
            //debug(newi);
            if(st.count(newi)){
                continue;
            }
            dp.push_back(newi);
            st.insert(input[i] + dp[j]);
        }
    }


    cout<<dp.size()- 1<<endl;
    sort(dp.begin(), dp.end());
    for(int i = 1; i<dp.size(); i++){
        cout<<dp[i]<<" ";
    }
}
